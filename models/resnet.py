        #print("scale: ", x_scal, w_scal)

        if self.w_conv1_scal is None:
            w = self.conv1.weight.data
            wmax = w.max().abs()
            wmin = w.min().abs()
            self.w_conv1_scal = max(wmax,wmin)/2**(nbit-1)
            w_int = torch.round(w/self.w_conv1_scal)
            self.conv1.weight.data = w_int
